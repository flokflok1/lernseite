class ExamGenerateRequest(BaseModel):
    """
    AI exam generation request model

    Based on 09_KI-Pipeline.md:949-1043

    Example:
        >>> request = ExamGenerateRequest(
        ...     title="IHK FISI AP1 Simulation",
        ...     exam_standard="IHK_FISI_AP1",
        ...     difficulty="intermediate",
        ...     duration_minutes=90,
        ...     question_distribution={"mcq": 25, "fill_blanks": 10, "short_answer": 3, "case_study": 2},
        ...     source_module_ids=["uuid1", "uuid2", "uuid3"]
        ... )
    """
    title: str = Field(..., min_length=3, max_length=255, description="Exam title")
    description: Optional[str] = Field(None, description="Exam description")
    exam_standard: ExamStandard = Field(..., description="Exam standard (IHK, CompTIA, etc.)")
    difficulty: str = Field(default="intermediate", description="Difficulty level")
    duration_minutes: int = Field(default=90, ge=30, le=300, description="Exam duration")
    passing_score: int = Field(default=50, ge=0, le=100, description="Passing score percentage")
    total_points: int = Field(default=100, ge=1, description="Maximum points")
    question_distribution: Dict[str, int] = Field(
        ...,
        description="Question type distribution (e.g. {'mcq': 25, 'fill_blanks': 10})"
    )
    topic_coverage: Optional[Dict[str, int]] = Field(
        None,
        description="Topic coverage percentages (e.g. {'netzwerke': 40, 'hardware': 20})"
    )
    source_module_ids: List[str] = Field(
        default=[],
        description="Source module UUIDs to generate from"
    )

    @field_validator('difficulty')
    @classmethod
    def validate_difficulty(cls, v: str) -> str:
        """Validate difficulty level"""
        valid_levels = ['beginner', 'intermediate', 'advanced', 'expert']
        if v not in valid_levels:
            raise ValueError(f'Difficulty must be one of: {", ".join(valid_levels)}')
        return v

    @field_validator('question_distribution')
    @classmethod
    def validate_question_distribution(cls, v: Dict[str, int]) -> Dict[str, int]:
        """Validate question distribution"""
        if not v:
            raise ValueError('question_distribution cannot be empty')

        valid_types = {'mcq', 'true_false', 'fill_blanks', 'matching', 'short_answer', 'math_problem', 'case_study'}
        for qtype in v.keys():
            if qtype not in valid_types:
                raise ValueError(f'Invalid question type: {qtype}. Must be one of: {", ".join(valid_types)}')

        total = sum(v.values())
        if total < 5:
            raise ValueError('Total questions must be at least 5')
        if total > 200:
            raise ValueError('Total questions cannot exceed 200')

        return v

    model_config = ConfigDict(from_attributes=True)
