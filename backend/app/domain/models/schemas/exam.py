"""
LernsystemX Admin Exam Models

Pydantic models for admin exam management operations:
- Admin exam list response
- Admin exam detail response
- Admin exam create request
- Admin exam update request
- Admin exam generate request (AI)
- Exam question models

Phase C1.3 - KI-Prüfungs-Generator
Based on Dok 04 (Kurs-Architektur.md) and Dok 09 (KI-Pipeline.md)
"""

from typing import Optional, List, Dict, Any
from datetime import datetime
from pydantic import BaseModel, Field, field_validator, ConfigDict

from app.domain.models.enums import ExamType, ExamStandard, QuestionType


class ExamQuestionResponse(BaseModel):
    """
    Single exam question response model

    Example:
        >>> question = ExamQuestionResponse(
        ...     question_id="uuid",
        ...     exam_id="uuid",
        ...     question_type="mcq",
        ...     question_text="Was ist das OSI-Modell?",
        ...     data={"options": [...], "correct": 1},
        ...     solution={"answer": "...", "explanation": "..."},
        ...     points=2
        ... )
    """
    question_id: str = Field(..., description="Question UUID")
    exam_id: str = Field(..., description="Exam UUID")
    question_type: QuestionType = Field(..., description="Type of question")
    question_text: str = Field(..., description="The question text")
    data: Dict[str, Any] = Field(default={}, description="Question-specific data (JSONB)")
    solution: Dict[str, Any] = Field(default={}, description="Solution data (JSONB)")
    points: int = Field(default=1, ge=1, le=100, description="Points for this question")
    order_index: int = Field(default=1, description="Question order")

    model_config = ConfigDict(from_attributes=True)


